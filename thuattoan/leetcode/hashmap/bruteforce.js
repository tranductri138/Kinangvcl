/**
 * @param {number[]} deck
 * @return {boolean}
 */
function hasGroupsSizeX(deck) {
    const N = deck.length;
    const count = new Array(10000).fill(0);

    for (let c of deck) {
        count[c]++;
    }

    const values = [];
    for (let i = 0; i < 10000; ++i) {
        if (count[i] > 0) {
            values.push(count[i]);
        }
    }

    search: for (let X = 2; X <= N; ++X) {
        if (N % X === 0) {
            for (let v of values) {
                if (v % X !== 0) {
                    continue search;
                }
            }
            return true;
        }
    }

    return false;
};

function hasGroupsSizeX2(deck) {
    let groups = {};
    let min = 0;
    const nod = (x, y) => {
        if (y > x) return nod(y, x);
        if (!y) return x;
        return nod(y, x % y);
    }

    for (let i = 0; i < deck.length; i++) {
        groups[deck[i]] = groups[deck[i]] ? groups[deck[i]] + 1 : 1;
    }

    groups = Object.values(groups);
    min = Math.min(...groups);
    if (min === 1) return false;

    console.log(groups);

    for (let i = 0; i < groups.length; i++) {
        if (groups[i] === min) continue;
        if (groups[i] % min === 0) continue;

        let diff = nod(groups[i], min);
        if (diff !== 1 && Number.isInteger(diff)) {
            min = diff;
            continue;
        }

        return false;
    }

    return true;
}

function hasGroupsSizeX3(deck) {
    if (deck.length < 2) {
        return false
    }
    const gcd = (a, b) => {
        if (a == 0 || b == 0) {
            return Math.max(a, b);
        } else {
            return gcd(b, a % b);
        }
    };

    const summary = {};
    deck.forEach(d => {
        summary[d] = (summary[d] || 0) + 1;
    });
    const allX = Object.values(summary)

    let g = allX[0]
    for (let i = 1; i < allX.length; i++) {
        g = gcd(g, allX[i])
        if (g < 2) {
            return false
        }
    }
    return g > 1
}

